export function sum(first, second) {
    return BigInt(first) + BigInt(second);
}

export function sub(first, second) {
    return BigInt(first) - BigInt(second);
}

export function multiply(first, second) {
    return BigInt(first) * BigInt(second);
}

export function div(first, second) {
    return BigInt(first) / BigInt(second);
}
